# stringa (testo semplice) valore "str"
textString = "questo è un testo semplice"
# numeri interi valore "int"
numero = 1
# numeri decimali FLOAT valore "float"
decimale = 1.5
# booleani (true o false) valore "bool"
booleano = True
# si utilizza il type() per sapere il tipo di dato di una variabile
print(type(textString))
print(type(numero))
print(type(decimale))
print(type(booleano))